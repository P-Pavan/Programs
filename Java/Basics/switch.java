class Switch {
    public static void main(String args[]) {
        int x = 5;
        switch(x) {
            case 1: System.out.println("one");
            break;
            case 2: System.out.println("two");
            break;
            case 5: System.out.println("It's Five");
            break;
            default: break;
        }
    }
}
